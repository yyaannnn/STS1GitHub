bool estMajeur(int age) {
  if (age >= 18) {
    return true;
  } else {
    return false;
  }
}

int exposant(nombre, exposant) {
  return pow(nombre, exposant);
}

float TVA(prix) {
  tauxTVA = 0.196; // taux de TVA à 19,6%
  return prix * (1 + tauxTVA);
}

int plusGrand(a, b, c) {
  max = a;
  if (b > max) {
    max = b;
  }
  if (c > max) {
    max = c;
  }
  return max;
}


